# 错误处理配置区 ==========================================
set -e          # 任何命令返回非 0 状态立即退出
set -u          # 使用未定义变量时立即报错
set -o pipefail # 管道中任一命令失败则整体失败

# =============================================================================
# 日志函数（简化版）
# =============================================================================

# 日志函数
# 参数：$@ - 日志消息（支持多个参数，自动拼接）
# 输出：如果安装了 gum 则使用 gum log，否则使用 echo
# gum 日志级别支持：none, debug, info, warn, error, fatal
# gum 时间格式支持：datetime, time, date, none
# 示例：
#   log "普通消息"
#   log "多" "个" "参" "数"
#   log "变量值：$VAR"
log() {
    if command -v gum >/dev/null 2>&1; then
        gum log -l info -t datetime "$@"
    else
        echo "[$(date '+%Y-%m-%d %H:%M:%S')] [INFO] $*"
    fi
}

# =============================================================================
# 主逻辑
# =============================================================================

main() {
    log "脚本开始执行"
    log "hello world"
    log "这是一条日志消息"
    log "脚本执行完成"

    exit 0
}

main "$@"
